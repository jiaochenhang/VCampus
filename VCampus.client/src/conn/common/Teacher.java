package conn.common;

public class Teacher {
	
	protected String uCard;
	protected String uRole;
	protected String uID;
	protected String uName;
	protected String uSex;
	protected String uCourse;
	
	public String getUCard() {
		return uCard;
	}
	
	public  void setUCard(String uCard) {
		this.uCard = uCard;
	}
	
	public String getURole() {
		return uRole;
	}
	
	public  void setURole(String uRole) {
		this.uRole = uRole;
	}
	
	public String getUID() {
		return uID;
	}
	
	public  void setUID(String uID) {
		this.uID = uID;
	}
	
	
	public String getUName() {
		return uName;
	}
	
	public  void setUName(String uName) {
		this.uName = uName;
	}
	
	public String getUSex() {
		return uSex;
	}
	
	public  void setUSex(String uSex) {
		this.uSex = uSex;
	}
	
	public String getUCourse() {
		return uCourse;
	}
	
	public  void setUCourse(String uCourse) {
		this.uCourse = uCourse;
	}
	
}
